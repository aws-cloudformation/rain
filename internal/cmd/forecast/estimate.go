// Estimates is a map of resource type name to ResourceEstimates, which are based on historical averages
var Estimates map[string]ResourceEstimate

// ResourceEstimate stores the estimated time, in seconds, to create,
// update, or delete a specific resource type.
// The Name is something like "AWS::S3::Bucket"
type ResourceEstimate struct {
	Name   string
	Create int
	Update int
	Delete int
}

// NewResourceEstimate creates a new instance of ResourceEstimate
func NewResourceEstimate(name string, create int, update int, del int) ResourceEstimate {
	est := ResourceEstimate{}
	est.Name = name
	est.Create = create
	est.Update = update
	est.Delete = del
	return est
}

type StackAction string

const (
	Create StackAction = "create"
	Update StackAction = "update"
	Delete StackAction = "delete"
)

// GetResourceEstimate returns the estimated time an action will take for the given resource type
func GetResourceEstimate(resourceType string, action StackAction) (int, error) {

	est, exists := Estimates[resourceType]
	if exists {
		switch action {
		case Create:
			return est.Create, nil
		case Update:
			return est.Update, nil
		case Delete:
			return est.Delete, nil
		}
		return 0, nil
	} else {
		return 0, fmt.Errorf("no estimate available for %v", resourceType)
	}
}

// getResourceType returns the resource type for the logical id
// For example, "MyBucket" would return "AWS::S3::Bucket" if it is present,
// otherwise "" is returned
func getResourceType(t cft.Template, logicalId string) string {
	rootMap := t.Node.Content[0]
	_, resources := s11n.GetMapValue(rootMap, "Resources")
	if resources == nil {
		panic("Expected to find a Resources section in the template")
	}
	for i, r := range resources.Content {
		if i%2 != 0 {
			continue
		}
		if logicalId == r.Value {
			resource := resources.Content[i+1]
			_, typeNode := s11n.GetMapValue(resource, "Type")
			if typeNode == nil {
				panic(fmt.Sprintf("Expected %v to have a Type", logicalId))
			}
			return typeNode.Value
		}
	}
	return ""
}

// addDurations is a recursive function that dives down dependencies to add
// the total duration estimated for stack actions
func addDurations(
	g graph.Graph,
	t cft.Template,
	action StackAction,
	dependencies []graph.Node,
	parentDuration *int,
	indent string) {

	if len(dependencies) == 0 {
		return
	}

	maxDuration := 0

	for _, d := range dependencies {
		if d.Type != "Resources" {
			continue
		}
		drt := getResourceType(t, d.Name)
		if drt == "" {
			panic(fmt.Sprintf("unexpected: no Type for %v", d.Name))
		}
		dd, err := GetResourceEstimate(drt, action)
		if err != nil {
			config.Debugf("no estimate for %v", drt)
			continue
		}
		config.Debugf("%v- depends on %v (%vs)", indent, d.Name, dd)

		childDuration := dd
		addDurations(g, t, action, g.Get(d), &childDuration, indent+"  ")
		if maxDuration < childDuration {
			maxDuration = childDuration
		}
	}

	*parentDuration += maxDuration
}

// PredictTotalEstimate returns the total number of seconds expected to deploy the stack.
// This function takes into account resources that will be deployed in parallel.
func PredictTotalEstimate(t cft.Template, stackExists bool) int {
	total := 0

	var action StackAction
	action = "create"
	if stackExists {
		action = "update"
	}

	// Build a graph of dependencies
	g := graph.New(t)

	config.Debugf("g:\n%v", g.String())

	// Dive down each path, add up durations. Longest duration from the top wins.
	//
	// (Downward is "depends on")
	//
	//       A   E  F      A: 2s
	//      / \ /    \     B: 4s
	//     B   C      G    C: 1s
	//          \          D: 6s
	//           D         E: 8s
	//                     F: 2s
	//                     G: 1s
	//
	// Deployment order:
	//
	// B-A		= 6s
	// D-C-A    = 9s
	// D-C-E    = 15s
	// G-F      = 3s
	//
	// Expected deployment time is 15s

	nodes := g.Nodes()
	for _, n := range nodes {
		if n.Type != "Resources" {
			continue
		}
		// For every node in the graph, walk down its dependencies and
		// add up the total duration.

		resourceType := getResourceType(t, n.Name)
		if resourceType == "" {
			panic(fmt.Sprintf("unexpected: no Type for %v", n.Name))
		}
		duration, err := GetResourceEstimate(resourceType, action)
		if err != nil {
			config.Debugf("no estimate for %v", resourceType)
			continue
		}
		config.Debugf("%v duration: %v", n.Name, duration)

		dependencies := g.Get(n)
		addDurations(g, t, action, dependencies, &duration, "  ")

		config.Debugf("total duration for %v is %vs", n.Name, duration)
		if total < duration {
			total = duration
		}
	}

	return total
}

// FormatEstimate returns a string in human readable format to represent the number of seconds.
// For example, 61 would return "0h, 1m, 1s"
func FormatEstimate(total int) string {
	return fmt.Sprintf("%vh, %vm, %vs", total/360, total/60, total%60)
}

